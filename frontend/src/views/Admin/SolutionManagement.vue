<script lang="ts" setup>
import type {
  AdminSolutionListQuery,
  AdminSolutionListQueryResult,
  ExportFormat,
  JudgeStatus,
  Language,
} from '@putongoj/shared'
import { AdminSolutionListQuerySchema } from '@putongoj/shared'
import Button from 'primevue/button'
import Column from 'primevue/column'
import DataTable from 'primevue/datatable'
import IconField from 'primevue/iconfield'
import InputIcon from 'primevue/inputicon'
import InputNumber from 'primevue/inputnumber'
import Paginator from 'primevue/paginator'
import Select from 'primevue/select'
import Tag from 'primevue/tag'
import { computed, onMounted, ref } from 'vue'
import { useI18n } from 'vue-i18n'
import { useRoute, useRouter } from 'vue-router'
import { exportSolutions, findSolutions } from '@/api/admin'
import ExportDialog from '@/components/ExportDialog.vue'
import UserFilter from '@/components/UserFilter.vue'
import {
  judgeStatusLabels,
  judgeStatusOptions,
  languageLabels,
  languageOptions,
} from '@/utils/constant'
import { exportDataToFile } from '@/utils/export'
import {
  getJudgeStatusClassname,
  getSimilarityClassname,
  thousandSeparator,
  timePretty,
} from '@/utils/formate'
import { onRouteQueryUpdate } from '@/utils/helper'
import { useMessage } from '@/utils/message'

const { t } = useI18n()
const route = useRoute()
const router = useRouter()
const message = useMessage()

const query = ref({} as AdminSolutionListQuery)
const docs = ref([] as AdminSolutionListQueryResult['docs'])
const total = ref(0)
const loading = ref(false)
const selectedDocs = ref([] as AdminSolutionListQueryResult['docs'])
const exportDialog = ref(false)

const hasFilter = computed(() => {
  return Boolean(
    query.value.user
    || query.value.problem
    || query.value.contest
    || Number.isInteger(query.value.judge)
    || query.value.language,
  )
})

async function fetch () {
  const parsed = AdminSolutionListQuerySchema.safeParse(route.query)
  if (parsed.success) {
    query.value = parsed.data
  } else {
    router.replace({ query: {} })
    return
  }

  loading.value = true
  const resp = await findSolutions(query.value)
  loading.value = false
  if (!resp.success) {
    message.error(t('ptoj.failed_fetch_solutions'), resp.message)
    docs.value = []
    total.value = 0
    return
  }

  docs.value = resp.data.docs
  total.value = resp.data.total
}

function onSort (event: any) {
  router.replace({
    query: {
      ...route.query,
      sortBy: event.sortField,
      sort: event.sortOrder,
    },
  })
}

function onPage (event: any) {
  router.replace({
    query: {
      ...route.query,
      page: (event.first / event.rows + 1),
    },
  })
}

function onSearch () {
  router.replace({
    query: {
      ...route.query,
      user: query.value.user || undefined,
      problem: query.value.problem || undefined,
      contest: query.value.contest || undefined,
      judge: Number.isInteger(query.value.judge) ? query.value.judge : undefined,
      language: query.value.language || undefined,
      page: undefined,
    },
  })
}

function onReset () {
  router.replace({
    query: {
      ...route.query,
      user: undefined,
      problem: undefined,
      contest: undefined,
      judge: undefined,
      language: undefined,
      page: undefined,
    },
  })
}

function onView (data: any) {
  router.push({ name: 'solution', params: { sid: data.sid } })
}
function onViewUser (data: any) {
  router.push({ name: 'UserProfile', params: { uid: data.uid } })
}
function onViewProblem (data: any) {
  router.push({ name: 'problemInfo', params: { pid: data.pid } })
}
function onViewContest (data: any) {
  if (!data.mid || data.mid <= 0) return
  router.push({ name: 'contestOverview', params: { cid: data.mid } })
}

async function onExport (format: ExportFormat) {
  message.info(t('ptoj.exporting_data'), t('ptoj.exporting_data_detail'))
  const resp = await exportSolutions(query.value)
  if (!resp.success) {
    message.error(t('ptoj.failed_fetch_solutions'), resp.message)
    exportDialog.value = false
    return
  }

  const filename = `PutongOJ_Solutions_${Date.now()}`
  try {
    exportDataToFile(resp.data, filename, format)
  } catch (err: any) {
    message.error(t('ptoj.failed_export_data'), err.message)
  }
  exportDialog.value = false
}

onMounted(fetch)
onRouteQueryUpdate(fetch)
</script>

<template>
  <div class="max-w-[1440px] p-0">
    <div class="p-6 border-b border-surface">
      <div class="flex items-center gap-4 mb-4 font-semibold">
        <i class="text-2xl pi pi-copy" />
        <h1 class="text-xl">
          {{ t('ptoj.solution_management') }}
        </h1>
      </div>
      <div class="grid items-end grid-cols-1 gap-4 lg:grid-cols-3 md:grid-cols-2 xl:grid-cols-4">
        <UserFilter v-model="query.user" :disabled="loading" @select="onSearch" />

        <IconField>
          <InputNumber
            v-model="query.problem" mode="decimal" :min="1" fluid :use-grouping="false"
            :placeholder="t('ptoj.filter_by_problem')" :disabled="loading" @keypress.enter="onSearch"
          />
          <InputIcon class="pi pi-flag" />
        </IconField>

        <IconField>
          <InputNumber
            v-model="query.contest" mode="decimal" :min="-1" fluid :use-grouping="false"
            :placeholder="t('ptoj.filter_by_contest')" :disabled="loading" @keypress.enter="onSearch"
          />
          <InputIcon class="pi pi-trophy" />
        </IconField>

        <Select
          v-model="query.judge" fluid :options="judgeStatusOptions" option-label="label" option-value="value"
          show-clear :placeholder="t('ptoj.filter_by_judge_status')" :disabled="loading" @change="onSearch"
        >
          <template #option="slotProps">
            <div :class="getJudgeStatusClassname(slotProps.option.value as JudgeStatus)">
              {{ slotProps.option.label }}
            </div>
          </template>
          <template #dropdownicon>
            <i class="pi pi-check-square" />
          </template>
        </Select>

        <Select
          v-model="query.language" fluid :options="languageOptions" option-label="label" option-value="value"
          show-clear :placeholder="t('ptoj.filter_by_language')" :disabled="loading" @change="onSearch"
        >
          <template #dropdownicon>
            <i class="pi pi-code" />
          </template>
        </Select>

        <div class="flex items-center justify-end gap-2 xl:col-span-3">
          <Button
            icon="pi pi-file-export" severity="secondary" outlined :disabled="loading"
            @click="exportDialog = true"
          />
          <Button icon="pi pi-refresh" severity="secondary" outlined :disabled="loading" @click="fetch" />
          <Button
            icon="pi pi-filter-slash" severity="secondary" outlined :disabled="loading || !hasFilter"
            @click="onReset"
          />
          <Button :label="t('ptoj.search')" icon="pi pi-search" :disabled="loading" @click="onSearch" />
        </div>
      </div>
    </div>

    <DataTable
      v-model:selection="selectedDocs" class="-mb-px whitespace-nowrap" :value="docs" sort-mode="single"
      :sort-field="query.sortBy" data-key="sid" :sort-order="query.sort" :lazy="true" :loading="loading" scrollable
      @sort="onSort"
    >
      <Column selection-mode="multiple" class="w-10 pl-6" frozen />

      <Column field="sid" class="font-medium text-center" frozen>
        <template #header>
          <span class="w-full text-center">
            <i class="pi pi-hashtag" />
          </span>
        </template>
        <template #body="{ data }">
          <a @click="onView(data)">
            {{ data.sid }}
          </a>
        </template>
      </Column>

      <Column :header="t('ptoj.user')" field="uid" class="font-medium truncate max-w-36 md:max-w-48 min-w-36">
        <template #body="{ data }">
          <a @click="onViewUser(data)">
            {{ data.uid }}
          </a>
        </template>
      </Column>

      <Column field="pid" class="text-center">
        <template #header>
          <span class="w-full font-semibold text-center">
            {{ t('ptoj.problem') }}
          </span>
        </template>
        <template #body="{ data }">
          <a @click="onViewProblem(data)">
            {{ data.pid }}
          </a>
        </template>
      </Column>

      <Column field="mid" class="text-center">
        <template #header>
          <span class="w-full font-semibold text-center">
            {{ t('ptoj.contest') }}
          </span>
        </template>
        <template #body="{ data }">
          <a v-if="data.mid && data.mid > 0" @click="onViewContest(data)">
            {{ data.mid }}
          </a>
          <span v-else>
            -
          </span>
        </template>
      </Column>

      <Column :header="t('ptoj.judge_status')" field="judge">
        <template #body="{ data }">
          <span :class="getJudgeStatusClassname(data.judge as JudgeStatus)">
            {{ judgeStatusLabels[data.judge as JudgeStatus] }}
          </span>
          <Tag
            v-if="data.sim" severity="secondary" class="ml-2 -my-px text-xs"
            :class="getSimilarityClassname(data.sim)"
          >
            {{ data.sim }}%
          </Tag>
        </template>
      </Column>

      <Column field="time" class="text-right" sortable>
        <template #header>
          <span class="w-full font-semibold text-right">
            {{ t('ptoj.time') }}
          </span>
        </template>
        <template #body="{ data }">
          {{ thousandSeparator(data.time) }} <small>ms</small>
        </template>
      </Column>

      <Column field="memory" class="text-right" sortable>
        <template #header>
          <span class="w-full font-semibold text-right">
            {{ t('ptoj.memory') }}
          </span>
        </template>
        <template #body="{ data }">
          {{ thousandSeparator(data.memory) }} <small>KB</small>
        </template>
      </Column>

      <Column field="language" class="text-center">
        <template #header>
          <span class="w-full font-semibold text-center">
            {{ t('ptoj.language') }}
          </span>
        </template>
        <template #body="{ data }">
          {{ languageLabels[data.language as Language] }}
        </template>
      </Column>

      <Column :header="t('ptoj.submitted_at')" field="createdAt" sortable>
        <template #body="{ data }">
          {{ timePretty(data.createdAt) }}
        </template>
      </Column>

      <Column class="w-20 px-6 py-1">
        <template #body="{ data }">
          <div class="flex items-center gap-1">
            <Button icon="pi pi-eye" text @click="onView(data)" />
          </div>
        </template>
      </Column>

      <template #empty>
        <span class="px-2">
          {{ t('ptoj.empty_content_desc') }}
        </span>
      </template>
    </DataTable>

    <Paginator
      class="sticky bottom-0 z-10 overflow-hidden border-t border-surface md:rounded-b-xl"
      :first="(query.page - 1) * query.pageSize" :rows="query.pageSize" :total-records="total"
      :current-page-report-template="t('ptoj.paginator_report')"
      template="FirstPageLink PrevPageLink CurrentPageReport NextPageLink LastPageLink" @page="onPage"
    />

    <ExportDialog v-model:visible="exportDialog" :estimated-count="total" @export="onExport" />
  </div>
</template>
